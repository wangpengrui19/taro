function buildMinor(suitKey) {
  return rankSlug.map((slug, i) => {
    const nameZh = `${suitZh[suitKey]}${rankZh[i]}`;
    const nameEn = `${rankEn[i]} of ${suitEn[suitKey]}`;
    const id = `${suitKey}-${String(i + 1).padStart(2, '0')}`;
    const image = makeImg(`${slug}-of-${suitSlug[suitKey]}`);
    const keywords = minorKeywords[suitKey][i];
    const astrology = suitAstrology[suitKey][i + 1] || '';
    const base = {
      id,
      nameZh,
      nameEn,
      group: suitKey,
      suit: suitKey,
      number: i + 1,
      element: suitElement[suitKey] + astrology,
      image,
      keywords
    };
    // 王牌使用 aceDemos
    if (i === 0 && aceDemos[suitKey]) {
      return { ...base, ...aceDemos[suitKey] };
    }
    // 其他牌使用 minorCardContents
    if (minorCardContents[suitKey] && minorCardContents[suitKey][i + 1]) {
      return { ...base, ...minorCardContents[suitKey][i + 1] };
    }
    // 如果没有内容，使用占位符
    return { ...base, ...placeholder(nameZh) };
  });
}

